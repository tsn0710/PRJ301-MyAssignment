public class Week {
    int no;
    Date dfrom;
    Date dto;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public Date getDfrom() {
        return dfrom;
    }

    public void setDfrom(Date dfrom) {
        this.dfrom = dfrom;
    }

    public Date getDto() {
        return dto;
    }

    public void setDto(Date dto) {
        this.dto = dto;
    }
    
    
}
